                <h2 class="change-font-size">Deficiências cognitivas</h2>
                <h3 class="change-font-size">Deficiência cognitiva Clínica vs Funcional</h3>
                <p class="p-2 change-font-size">
                    Existem pelo menos duas maneiras de classificar deficiências cognitivas: pela incapacidade
                    funcional ou por incapacidade clínica. Diagnósticos clínicos de deficiências

                    cognitivas incluem autismo, Síndrome de Down, traumatismos crânio-encefálicos (TCE), até a
                    demência. Condições cognitivas menos severas incluem déficit de atenção e

                    desordem (ADD), dislexia (dificuldade em leitura), discalculia (dificuldade com a matemática), e
                    dificuldades de aprendizagem em geral. Diagnósticos clínicos podem ser

                    úteis a partir de uma perspectiva médica para o tratamento, mas para efeitos de acessibilidade
                    web, classificar as deficiências cognitivas por incapacidade funcional é

                    mais útil. Em se tratando de Acessibilidade Web, no caso da deficiência física ou comportamental
                    as causas médicas da deficiência são ignoradas, tendo foco sobre a

                    incidência das habilidades e os desafios resultantes. Algumas das principais categorias dos
                    déficits funcionais e cognitivos incluem deficiência ou dificuldades de:

                </p>
                <ul>
                    <li class="change-font-size">Memória.</li>
                    <li class="change-font-size">Resolução de problemas.</li>
                    <li class="change-font-size">Atenção.</li>
                    <li class="change-font-size">Compreensão Verbal, de Leitura e Lingüística.</li>
                    <li class="change-font-size">Compreensão Matemática.</li>
                    <li class="change-font-size">Compreensão Visual.</li>
                </ul>
