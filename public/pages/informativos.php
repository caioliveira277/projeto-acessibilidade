                <h2 class="change-font-size">Deficiências visuais</h2>
                <h3 class="change-font-size">Cego:</h3>
                <p class="p-2 change-font-size">A cegueira pressupõe a falta de percepção visual devido a fatores fisiológicos ou neurológicos. A
                    cegueira total ou simplesmente amaurose, caracteriza-se pela completa

                    perda de visão sem percepção visual de luz e forma. A cegueira pode ser congênita ou adquirida.
                </p>

                <p class="p-2 change-font-size">As pessoas cegas necessitam do sistema de escrita e leitura em relevo denominada Sistema Braille.
                    Tecnologias assistivas representam um enorme avanço para pessoas com

                    cegueira, como os softwares leitores de tela e os livros digitais acessíveis MEC Daisy.</p>

                <h3 class="change-font-size">Baixa Visão:</h3>
                <p class="p-2 change-font-size">
                    A acuidade visual das pessoas com baixa visão é muito variável; mas, em geral, baixa visão é
                    definida como uma condição na qual a visão da pessoa não pode ser

                    totalmente corrigida por óculos, interferindo em suas atividades diárias, assim como a leitura e
                    a locomoção.
                </p>

                <p class="p-2 change-font-size">
                    A baixa visão é o resultado de condições oftalmológicas como degeneração macular, glaucoma,
                    retinopatia diabética, ou catarata. Cada uma destas condições causam

                    diferentes tipos de efeitos na visão da pessoa, dificultando suas atividades pessoais. As
                    pessoas com baixa visão necessitam de auxílios ópticos como óculos, lentes

                    corretivas, lupas simples e/ou eletrônicas, e não ópticos que se caracterizam pelos textos com
                    caracteres ampliados e uso de tecnologias assistivas como softwares

                    ampliadores e leitores de tela e os livros digitais acessíveis MEC Daisy.
                </p>
